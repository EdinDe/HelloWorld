public class forloops_2 {

    public static void main(String[] args) {

        for (int i = 2; i <= 12; i = i + 2){
            System.out.println(i);
        }


        for (int i = 4; i <= 79; i = i + 15){
            System.out.println(i);
        }

        for (int i = 30; i >= -20; i = i -10){
            System.out.println(i);
        }

        for (int i = -7; i <= 13; i = i +4){
            System.out.println(i);
        }

        for (int i = 97; i >= 82; i = i -3){
            System.out.println(i);
        }









    }
}
